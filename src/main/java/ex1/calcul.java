public class calcul {
/**
 * Additionne le paramètre a avec le paramètre b
 * @param a
 * @param b
 * @return
 */
	public int Addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Soustrait le paramètre a avec le paramètre b
	 * @param a
	 * @param b
	 * @return
	 */
	public int Soustraction(int a, int b){
		return a-b;
	}
}
